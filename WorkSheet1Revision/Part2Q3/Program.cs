using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Part2Q3
{
    class Program
    {
        static void Main(string[] args)
        {
            /*
            3.Write a for loop to print all integers between 1 and 20 in descending order.
            */

            for (int i = 20; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
